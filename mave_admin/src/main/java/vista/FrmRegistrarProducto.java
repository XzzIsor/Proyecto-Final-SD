    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtCodigo.getText().isEmpty()) {
            if (!txtNombre.getText().isEmpty()) {
                if (!txtPrecio.getText().isEmpty()) {

                    Product producto = new Product(Integer.valueOf(txtCodigo.getText()), txtNombre.getText(),
                            "En oferta", Integer.valueOf(txtPrecio.getText()));

                    Product objProductoRegistrado = objProductoServices.registerProduct(producto);

                    if (objProductoRegistrado != null) {
                        JOptionPane.showMessageDialog(this, "Producto Registrado Correctamente.");
                        limpiarCamposFormulario();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se ha podido registrar el producto.");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Ingrese un precio para el producto.");
                    txtPrecio.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese un nombre.");
                txtNombre.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese un codigo para el producto.");
            txtCodigo.requestFocus();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void limpiarCamposFormulario() {
        txtCodigo.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
    }

